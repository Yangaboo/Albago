package com.albago.webservice;

public class URL {
    private String url;
    private String pwd;

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getPwd() {
        return pwd;
    }

    public String getUrl() {
        return pwd;
    }
}
